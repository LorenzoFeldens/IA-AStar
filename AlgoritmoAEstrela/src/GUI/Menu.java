    /*  Funçao botão OK:
     *   -Verifica valores inseridos
     *   -Testa se o problema tem solução
     *      --Caso exista permite a exibição da Árvore, Caminho e Resultado
     */
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int grid = (int) jSpinner1.getValue();
        int obs = (int) jSpinner2.getValue();
        
        int xIni = (int) jSpinner3.getValue();
        int yIni = (int) jSpinner4.getValue();
        int xFin = (int) jSpinner5.getValue();
        int yFin = (int) jSpinner6.getValue();
        
        if(xIni>grid-1 || yIni>grid-1 || xFin>grid-1 || yFin>grid-1){
            JOptionPane.showMessageDialog(null, "As posições devem ser"
                    + "dentro do grid!");                                       
            jButton2.setEnabled(false);
            jButton3.setEnabled(false);
            jButton5.setEnabled(false);
        }else{
            algoritmo = new Algoritmo(grid, obs, xIni, yIni, xFin, yFin);
            jButton2.setEnabled(true);
            jButton5.setEnabled(true);
            
            if(algoritmo.executa()){
            jButton3.setEnabled(true);
            }else{
                JOptionPane.showMessageDialog(null, "Problema Gerado não possui solução,\n"
                    + "Caminho não disponível!");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

